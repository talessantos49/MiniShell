#include "libft.h"

int	ft_isalnum(int c)
{
	int	r;

	r = ((c >= 'a' && c <= 'z')
			|| (c >= 'A' && c <= 'Z')
			|| (c >= '0' && c <= '9'));
	return (r);
}
