#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*res;

	i = 0;
	res = (char *) malloc((ft_strlen(str) + 1) * sizeof(char));
	if (!res)
		return (NULL);
	while (str[i])
	{
		res[i] = str[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
