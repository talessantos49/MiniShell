#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n >= -2147483647 && n <= 2147483647)
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		if (n >= 10)
		{
			ft_putnbr_fd(n / 10, fd);
			ft_putnbr_fd(n % 10, fd);
		}
		else
		{
			n += '0';
			write(fd, &n, 1);
		}
	}
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
}
